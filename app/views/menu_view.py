def show_main_menu(role):
    """
    Показать главное меню в зависимости от роли пользователя.
    :param role: 'student', 'teacher', 'admin'
    """
    print("\n" + "-" * 50)
    print("           МЕНЮ")
    print("-" * 50)

    if role == 'student':
        print("1. Просмотреть все олимпиады")
        print("2. Просмотреть детали олимпиады")
        print("3. Зарегистрироваться на олимпиаду")
        print("4. Мои результаты")
        print("0. Выход")

    elif role == 'teacher':
        print("1. Просмотреть все олимпиады")
        print("2. Просмотреть детали олимпиады")
        print("3. Добавить положение")
        print("4. Ввод результатов")
        print("5. Просмотреть участников")
        print("0. Выход")

    elif role == 'admin':
        print("1. Просмотреть все олимпиады")
        print("2. Добавить олимпиаду")
        print("3. Создать пользователя")
        print("4. Удалить олимпиаду")
        print("5. Просмотреть файлы")
        print("0. Выход")

    else:
        print("1. Просмотреть все олимпиады")
        print("0. Выход")